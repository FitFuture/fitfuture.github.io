		<h1 id="contenthead">Soccer</h1><br>
		<h2>History</h2>
		<p>Evidence shows people in China during the 3rd century B.C. played a sport roughly resembling soccer by dribbling a ball and kicking it into a net. Romans and Greeks also have evidence supporting them playing ball for “fun and frolic”. It was a much different sport in medieval times, as were most sports, and of course, the sport lacked much regulation and games often had violence. Modern soccer started in Britain, co-founded by England and Scotland. Much like football, it became heavily regulated to avoid brutality in the sport. However, some schools chose to keep rugby-rules. The foundation for soccer was established in 1869, which banned handling the ball in any kind. The sport spread rapidly as English influence spread it to the other portions of the world. FIFA was established in 1904, and different leagues were formed between different countries, and the first “World Cup” was held in Uruguay.</p>
		<h2>How to Play</h2>
		<div class="img">
			<img src="images/socc1.png" alt="Field" width="150px" height="150px">
			<div class="desc">The teams are composed of 11 players, though one player will be designated as a goalkeeper or "goallie". You are allowed substitutes for players, but you only get three at the professional level. The game is simple, you have to get the ball into the opposing team's goal without touching the ball or other players. The court is designed into two halves, bordered by "touchlines", which don't have goals on them, and "goal lines", which do. </div>
		</div>
		<div class="img">
			<img src="images/socc2.png" alt="Kick" width="150px" height="150px">
			<div class="desc">Penalties come in the form of Red Cards and Yellow Cards. Yellow cards are given to players who have committed minor offenses, such as unsportsmanlike conduct or trying to enter or leave the field without a referee's approval. The red card will eject you from a game, and are given for serious offenses, such as violent conduct, spitting at someone, or denying a goal oppratunity by handling the ball (and not being the goallie). You can also get a red card when you should be given a second yellow card, so remember you only get one warning!</div>
		</div>
		<div class="img">
			<img src="images/socc3.png" alt="Goooooooaaaaaaallllll!!!!!" width="150px" height="150px">
			<div class="desc">All scores in soccer worth one point, with no extra point opportunity. After a goal is made, the ball is placed back in the middle of the field to start the game again. The opposing team will then kick it off to get it as deep into rival territory as possible. Other means to restart play include a throw-in, when the ball crosses the touchline; a goal or corner kick, when the offense or defense (respectively) kicked the ball across the goal line but not necessarily made a goal, allowing the opposing team to kick it back in; or a penalty kick, when a foul inside the opponent's penalty box allows the fouled team to get a free kick.</div>
		</div>
		<div class="img">
			<img src="images/socc4.png" alt="Block" width="150px" height="150px">
			<div class="desc">In soccer, since nobody ever gets true control of the ball, except for the goallie in some situations, the defense has an assortment of options to gain the upperhand. They can attempt to block shots or passes with any part of their body aside from their hands. They can also directly attempt to "steal" the ball, simply by kicking it away from whoever is driving it down the field.</div>
		</div>
		<div class="img">
			<img src="images/socc5.png" alt="Scoreboard" width="150px" height="150px">
			<div class="desc">A match of soccer is divided into 2 periods or halves which run for 45 minutes each. Between halves, there is a 15 minute break, meaning average soccer games last 100 minutes. The clock never stops ticking during these halves, even when the ball is out of play. Time only stops when the referee allows for a delay of game due to injured players requiring treatment (or, at least, removal from the field), to allow substitutes to take the field, or other possible reasons. Time may be added, also, in the situation that a game may end without a penalty kick being taken. In games that aren't championship games, the ending is allowed to be a draw. In championship games, 2 more periods of 15 minutes are allowed, and if those end with a tie still, a "penalty shootout" may occur to determine the winner while not actually effecting the score record.</div>
